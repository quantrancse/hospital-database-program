  CREATE OR REPLACE PROCEDURE "MANAGER"."PROCEDURE3" (patientID IN CHAR, CURSORNAME OUT SYS_REFCURSOR) AS
BEGIN 
    IF INSTR(patientID, 'IP') > 0 THEN
		-- INPATIENT, treatment only
        OPEN CURSORNAME FOR
		SELECT MID, MNAME, MEFFECTS, MPRICE
		FROM Uses_Treat NATURAL JOIN MEDICATION
		WHERE PID_In = patientID;
	ELSE
		-- OUTPATIENT, examination only
        OPEN CURSORNAME FOR
		SELECT MID, MNAME, MEFFECTS, MPRICE
		FROM Uses_Exam NATURAL JOIN MEDICATION
		WHERE PID_Out = patientID;
    END IF;    
END;


