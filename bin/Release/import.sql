  CREATE OR REPLACE PROCEDURE "MANAGER"."PROCEDURE4" (startDate IN DATE, endDate IN DATE, CURSORNAME OUT SYS_REFCURSOR) AS
	
BEGIN
    OPEN CURSORNAME FOR 
SELECT COUNT(CHECK_DATE.PID) AS PATIENT_NUM, DOCTOR.EID_Doc AS EID_DOC
FROM
    (
        (
            (
                SELECT PID, EID_Doc
                FROM (  SELECT EID_Doc, PID_OUT AS PID, ExDate AS SDate
                        FROM EXAMINATION)
                WHERE startDate <= SDate AND SDate <= endDate
            )
                UNION
            (
                SELECT PID, EID_Doc
                FROM ( SELECT EID_Doc, PID_In AS PID, TrStart AS SDate, TrEnd AS EDate
                        FROM TREATMENT)
                WHERE (SDate >= startDate OR EDate >= endDate) AND (SDate <= endDate)
            )
        ) CHECK_DATE
            RIGHT JOIN 
        DOCTOR
            ON
        CHECK_DATE.EID_DOC = DOCTOR.EID_DOC         
    )
GROUP BY DOCTOR.EID_DOC
ORDER BY COUNT(CHECK_DATE.PID) ASC;
END;


